        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {

		/*下拉框*/
			let shangjiaTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shangjia_types',
            }
			let shangjiaTypes = await this.$api.page(`dictionary`, shangjiaTypesParams);
			this.shangjiaTypesOptions = shangjiaTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`shangjia`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.shangjiaId){
                this.ruleForm.shangjiaId = options.shangjiaId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            shangjiaTypesChange(e) {
                this.shangjiaTypesIndex = e.target.value
                this.ruleForm.shangjiaValue = this.shangjiaTypesOptions[this.shangjiaTypesIndex].indexName
                this.ruleForm.shangjiaTypes = this.shangjiaTypesOptions[this.shangjiaTypesIndex].codeIndex
            },


            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },


			shangjiaPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.shangjiaPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.username)) {
                    this.$utils.msg(`账户不能为空`);
                    return
                }
				if ((!this.ruleForm.shangjiaName)) {
                    this.$utils.msg(`商家名称不能为空`);
                    return
                }
				if ((!this.ruleForm.shangjiaPhone) && (!this.$validate.isMobile(this.ruleForm.shangjiaPhone))) {
                    this.$utils.msg(`联系方式不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.shangjiaEmail) && (!this.$validate.isEmail(this.ruleForm.shangjiaEmail))) {
                    this.$utils.msg(`邮箱不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.shangjiaPhoto)) {
                    this.$utils.msg(`营业执照展示不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`shangjia`, this.ruleForm);
                } else {
                    await this.$api.save(`shangjia`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
